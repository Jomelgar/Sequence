    void setComboUsers(){
        String[] usuarios = new String[Inicio.ManagerAcc.size()];
        int contador = 0;
        for(Usuario.Usuario usuario: Inicio.ManagerAcc.getUsers()){
            usuarios[contador] = usuario.getNombre();
            contador++;
        }
        
        ComboUsers.setModel(new javax.swing.DefaultComboBoxModel<>(usuarios));
    }
    
    void setListModel(){
        jList1.setModel(new javax.swing.AbstractListModel<String>(){
            String[] send = Users.toArray(new String[Users.size()]);
            String[] strings = send;
            public int getSize() { return strings.length; }
            public String getElementAt(int i) { return strings[i]; }
        });
    }
