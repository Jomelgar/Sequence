    void intercambiar(ImageIcon ficha){
        for(int i = 0 ; i < 10; i++){
            for(int c =0; c< 10; c++){
                if(!juego.tablero.ArraySequence[i][c] && 
                        (ImageIcon)juego.tablero.Fichas[i][c].getIcon() == ficha){
                    juego.tablero.Fichas[i][c].setIcon(juego.JugadorActual.getFicha());
                }
            }
        }
    }
